package com.energygames.lojadegames.enums;

/**
 * Enum que representa os diferentes tipos de imagens de um produto.
 * Permite organizar e categorizar as imagens de jogos.
 */
public enum TipoImagemEnum {
    /**
     * Capa oficial do jogo (cover art)
     */
    CAPA,
    
    /**
     * Screenshot do gameplay
     */
    SCREENSHOT,
    
    /**
     * Arte promocional ou artwork
     */
    ARTWORK,
    
    /**
     * Logo do jogo
     */
    LOGO,
    
    /**
     * Banner horizontal (usado em carrosséis)
     */
    BANNER,
    
    /**
     * Ícone do jogo (geralmente pequeno)
     */
    ICONE
}
