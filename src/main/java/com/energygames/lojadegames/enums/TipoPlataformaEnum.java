package com.energygames.lojadegames.enums;

/**
 * Enum que representa o tipo de plataforma de jogos.
 * Usado para categorizar e filtrar plataformas.
 */
public enum TipoPlataformaEnum {
    /**
     * Consoles de videogame (PlayStation, Xbox, Nintendo)
     */
    CONSOLE,
    
    /**
     * Computadores pessoais (Windows, Mac, Linux)
     */
    PC,
    
    /**
     * Dispositivos móveis (iOS, Android)
     */
    MOBILE,
    
    /**
     * Consoles portáteis (Nintendo Switch modo portátil, Steam Deck)
     */
    HANDHELD,
    
    /**
     * Máquinas de arcade
     */
    ARCADE,
    
    /**
     * Plataformas de realidade virtual (PlayStation VR, Oculus, HTC Vive)
     */
    VR,
    
    /**
     * Serviços de streaming de jogos (Google Stadia, GeForce Now)
     */
    CLOUD
}
