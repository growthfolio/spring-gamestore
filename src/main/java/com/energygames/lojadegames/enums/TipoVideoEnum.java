package com.energygames.lojadegames.enums;

/**
 * Enum que representa os tipos de vídeos de um produto.
 * Usado para categorizar trailers e outros vídeos relacionados.
 */
public enum TipoVideoEnum {
    /**
     * Trailer oficial do jogo
     */
    TRAILER,
    
    /**
     * Vídeo de gameplay
     */
    GAMEPLAY,
    
    /**
     * Review/análise do jogo
     */
    REVIEW,
    
    /**
     * Teaser (trailer curto)
     */
    TEASER,
    
    /**
     * Making of / behind the scenes
     */
    MAKING_OF,
    
    /**
     * Trailer de lançamento
     */
    LAUNCH_TRAILER,
    
    /**
     * Trailer de anúncio
     */
    ANNOUNCEMENT
}
